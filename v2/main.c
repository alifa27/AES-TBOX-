// Fonction d'initialisation du compteur DWT (Data Watchpoint and Trace)
// (Précision au cycle d'horloge près)
void DWT_Init(void) {
    CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk; // Active le module Trace
    DWT->CYCCNT = 0;       // Remise à zéro du compteur
    DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk; // Active le comptage des cycles
}
