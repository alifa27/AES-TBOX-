/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  /* USER CODE BEGIN 2 */

  // Démarrage du chronomètre matériel
  DWT_Init();

  // 2. Les données AES (Le message et la clé)
  // Déclarées ici (localement) : elles ne seront pas suivies par Live Expressions.
  uint32_t state[4] = {0x3243f6a8, 0x885a308d, 0x313198a2, 0xe0370734};
  uint32_t roundKeys[44] = {0};

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    // ========================================================================
    // ÉTAPE 1 : LE CHIFFREMENT (ENCRYPT)
    // ========================================================================
    start_cycles = DWT->CYCCNT; // Démarrage chrono

    // On chiffre le tableau 'state' (10 tours pour l'AES-128)
    AES_Encrypt_V2(state, roundKeys, 10);

    end_cycles = DWT->CYCCNT;   // Arrêt chrono

    // Calcul de la durée exacte
    duration_encrypt = end_cycles - start_cycles;

    // ========================================================================
    // ÉTAPE 2 : LE DÉCHIFFREMENT (DECRYPT)
    // ========================================================================
    start_cycles = DWT->CYCCNT; // Redémarrage chrono

    // On reprend le tableau 'state' chiffré et on le repasse dans la machine
    // en sens inverse pour retrouver le message de départ.
    AES_Decrypt_V2(state, roundKeys, 10);

    end_cycles = DWT->CYCCNT;   // Arrêt chrono

    // Calcul de la durée exacte
    duration_decrypt = end_cycles - start_cycles;

    // Pause d'une seconde. Mets un point d'arrêt ici pour lire les variables duration.
    HAL_Delay(1000);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

